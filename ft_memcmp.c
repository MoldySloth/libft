#include "libft.h"

int	ft_memcmp(const void *s1, const void *s2, size_t n)
{
	size_t			i;
	int				diff;
	unsigned char	*cs1;
	unsigned char	*cs2;

	i = 0;
	cs1 = (unsigned char *)s1;
	cs2 = (unsigned char *)s2;
	while (n > i)
	{
		diff = cs1[i] - cs2[i];
		if (diff)
			return (diff);
		i++;
	}
	return (0);
}
